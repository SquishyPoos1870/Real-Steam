[mod-setting-description]
real-steam-enable-runtime-effects=Turns all Real Steam runtime vapour effects on or off.
real-steam-density=Controls how often Real Steam emits visual vapour. High gives more visible leaks and machine steam; Low is best for very large factories. Version 1.0.9 keeps pipe leaks cleaner and less wind-smeared.
real-steam-include-boilers=Allows boilers and heat exchangers to emit soft working vapour.
real-steam-include-pipes=Allows steam pipes and pipe-to-ground joins to emit small leak wisps. Pipe leaks now prefer selected bad seals and pipe-to-ground joins instead of making whole pipe runs smoky.
real-steam-weather-integration=When Real Rain or Real Wind is installed, storms and gusts push steam/vapour harder while each mod remains optional and stand-alone.
real-steam-quality-reduces-leaks=When enabled, higher-quality steam equipment produces fewer Real Steam leak visuals. Normal quality keeps the standard look; uncommon, rare, epic, and legendary quality become progressively cleaner.
real-steam-replace-boiler-smoke=Startup option that swaps vanilla boiler exhaust smoke to Real Steam's softer pressure puff.

